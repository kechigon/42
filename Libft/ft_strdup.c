#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*cpy;

	len = ft_strlen(s) + 1;
	if (!(cpy = malloc(len)))
		return (NULL);
	return ((char *)ft_memcpy(cpy, s, len));
}
